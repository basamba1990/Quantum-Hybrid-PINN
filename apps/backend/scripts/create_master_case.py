"""
Script de création du Cas Master OpenFOAM paramétrable.
Génère une structure OpenFOAM complète et fonctionnelle qui peut être adaptée
à différentes géométries et conditions aux limites.
"""

import os
import shutil
from pathlib import Path
from typing import Dict, Any
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


class OpenFOAMMasterCaseGenerator:
    """Générateur de cas OpenFOAM Master paramétrable."""

    def __init__(self, base_path: str = "/home/ubuntu/cases"):
        """
        Initialiser le générateur.

        Args:
            base_path: Chemin de base pour les cas OpenFOAM.
        """
        self.base_path = Path(base_path)
        self.base_path.mkdir(parents=True, exist_ok=True)

    def create_master_case(self, case_name: str = "master_h2_pipeline") -> bool:
        """
        Créer un cas OpenFOAM Master complet.

        Args:
            case_name: Nom du cas à créer.

        Returns:
            True si succès, False sinon.
        """
        case_path = self.base_path / case_name
        logger.info(f"Creating master case at: {case_path}")

        # Créer les répertoires principaux
        try:
            self._create_directory_structure(case_path)
            self._create_system_files(case_path)
            self._create_constant_files(case_path)
            self._create_initial_conditions(case_path)
            logger.info(f"Master case created successfully at: {case_path}")
            return True
        except Exception as e:
            logger.error(f"Error creating master case: {e}")
            return False

    def _create_directory_structure(self, case_path: Path) -> None:
        """Créer la structure de répertoires."""
        directories = [
            "system",
            "constant",
            "constant/polyMesh",
            "0"
        ]

        for directory in directories:
            dir_path = case_path / directory
            dir_path.mkdir(parents=True, exist_ok=True)
            logger.info(f"Created directory: {dir_path}")

    def _create_system_files(self, case_path: Path) -> None:
        """Créer les fichiers système OpenFOAM."""

        # controlDict
        control_dict = """FoamFile
{
    version     2.0;
    format      ascii;
    class       dictionary;
    location    "system";
    object      controlDict;
}

application     simpleFoam;
startFrom       startTime;
startTime       0;
stopAt          endTime;
endTime         1000;
deltaT          1;
writeControl    timeStep;
writeInterval   100;
purgeWrite      0;
writeFormat     ascii;
writePrecision  6;
writeCompression off;
timeFormat      general;
timePrecision   6;
runTimeModifiable true;
"""
        (case_path / "system" / "controlDict").write_text(control_dict)
        logger.info("Created system/controlDict")

        # fvSchemes
        fv_schemes = """FoamFile
{
    version     2.0;
    format      ascii;
    class       dictionary;
    location    "system";
    object      fvSchemes;
}

ddtSchemes
{
    default         steadyState;
}

gradSchemes
{
    default         Gauss linear;
}

divSchemes
{
    default         none;
    div(phi,U)      bounded Gauss linearUpwind grad(U);
    div(phi,k)      bounded Gauss linearUpwind grad(k);
    div(phi,epsilon) bounded Gauss linearUpwind grad(epsilon);
    div((nuEff*dev2(T(grad(U))))) Gauss linear;
}

laplacianSchemes
{
    default         Gauss linear corrected;
}

interpolationSchemes
{
    default         linear;
}

snGradSchemes
{
    default         corrected;
}
"""
        (case_path / "system" / "fvSchemes").write_text(fv_schemes)
        logger.info("Created system/fvSchemes")

        # fvSolution
        fv_solution = """FoamFile
{
    version     2.0;
    format      ascii;
    class       dictionary;
    location    "system";
    object      fvSolution;
}

solvers
{
    p
    {
        solver          GAMG;
        tolerance       1e-06;
        relTol          0.1;
        smoother        GaussSeidel;
        nPreSweeps      0;
        nPostSweeps     2;
        nFinestSweeps   2;
        cacheAgglomeration on;
        nCellsInCoarsestLevel 10;
        agglomerator    faceAreaPair;
        mergeLevels     1;
    }

    U
    {
        solver          smoothSolver;
        smoother        GaussSeidel;
        nSweeps         2;
        tolerance       1e-05;
        relTol          0.1;
    }

    k
    {
        solver          smoothSolver;
        smoother        GaussSeidel;
        nSweeps         2;
        tolerance       1e-05;
        relTol          0.1;
    }

    epsilon
    {
        solver          smoothSolver;
        smoother        GaussSeidel;
        nSweeps         2;
        tolerance       1e-05;
        relTol          0.1;
    }
}

SIMPLE
{
    nNonOrthogonalCorrectors 0;
    residualControl
    {
        p               1e-3;
        U               1e-4;
        k               1e-4;
        epsilon         1e-4;
    }
}

relaxationFactors
{
    fields
    {
        p               0.3;
    }
    equations
    {
        U               0.7;
        k               0.7;
        epsilon         0.7;
    }
}
"""
        (case_path / "system" / "fvSolution").write_text(fv_solution)
        logger.info("Created system/fvSolution")

    def _create_constant_files(self, case_path: Path) -> None:
        """Créer les fichiers constants."""

        # Créer un maillage minimal (cube unitaire)
        self._create_simple_mesh(case_path)

        # transportProperties
        transport_props = """FoamFile
{
    version     2.0;
    format      ascii;
    class       dictionary;
    location    "constant";
    object      transportProperties;
}

nu              nu [0 2 -1 0 0 0 0] 1e-05;
rho             rho [1 -3 0 0 0 0 0] 1.0;
"""
        (case_path / "constant" / "transportProperties").write_text(transport_props)
        logger.info("Created constant/transportProperties")

        # turbulenceProperties
        turbulence_props = """FoamFile
{
    version     2.0;
    format      ascii;
    class       dictionary;
    location    "constant";
    object      turbulenceProperties;
}

simulationType  RAS;

RAS
{
    RASModel        kEpsilon;
    turbulence      on;
    printCoeffs     on;
}
"""
        (case_path / "constant" / "turbulenceProperties").write_text(turbulence_props)
        logger.info("Created constant/turbulenceProperties")

